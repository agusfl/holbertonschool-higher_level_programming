#!/usr/bin/python3
def print_list_integer(my_list=[]):
    for i in my_list:
        print(f"{i}")

# Este ejercicio es sencillo, hay que recorrer la lista "my_list" e ir imprimiendo cada item de la lista que seria: 'i'
